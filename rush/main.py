from checkmate import checkmate  # นำเข้าฟังก์ชัน checkmate จากไฟล์ checkmate.py

def main():  # ฟังก์ชันหลักของโปรแกรม
    # กำหนดกระดานหมากรุกตัวอย่างที่ 1 เป็นสตริงหลายบรรทัด
    board_example1 = """\
R...
.K..
..P.
....
"""
    
    # กำหนดกระดานหมากรุกตัวอย่างที่ 2 เป็นสตริงหลายบรรทัด
    board_example2 = """\
R...
.K..
"""
    
    # แสดงข้อความว่าเรากำลังทดสอบกระดานตัวอย่างที่ 1
    print("Testing Board Example 1:")
    
    # เรียกใช้ฟังก์ชัน checkmate เพื่อตรวจสอบว่ากระดานตัวอย่างที่ 1 อยู่ในสถานการณ์รุกจนหรือไม่
    checkmate(board_example1)  
    
    # แสดงข้อความว่าเรากำลังทดสอบกระดานตัวอย่างที่ 2
    print("Testing Board Example 2:")
    
    # เรียกใช้ฟังก์ชัน checkmate เพื่อตรวจสอบว่ากระดานตัวอย่างที่ 2 อยู่ในสถานการณ์รุกจนหรือไม่
    checkmate(board_example2)  

# ตรวจสอบว่าถ้าไฟล์นี้ถูกเรียกใช้งานโดยตรง (ไม่ใช่การนำเข้าเป็นโมดูล) จะเรียกใช้ฟังก์ชัน main()
if __name__ == "__main__":
    main()